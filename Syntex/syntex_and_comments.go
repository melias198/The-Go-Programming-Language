package main // In Go, every program is part of a package. We define this using the package keyword.

import "fmt" // import "fmt" lets us import files included in the fmt package.

func main() { // func main() is the entry point of every Go program.
	fmt.Println("Hello, World!") //  is a function made available from the fmt package. It is used to output/print text.
}

